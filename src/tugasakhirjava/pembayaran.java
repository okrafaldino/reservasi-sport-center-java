    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Preview
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.addColumn("No");
        tableModel.addColumn("No Transaksi");
        tableModel.addColumn("Tanggal Booking");
        tableModel.addColumn("Nama");
        tableModel.addColumn("Total Bayar");
        tableModel.addColumn("DP");
        tableModel.addColumn("Sisa");
        tableModel.addColumn("Bayar");
        tableModel.addColumn("Final Payment");
        tableModel.addColumn("Status");
        int no=1;
        try {
            Connection c=database.getkoneksi();
            sql = "SELECT * FROM `tbl_pembayaran`";
            Statement st=c.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                tableModel.addRow(new Object[]{(""+no++),rs.getString("No_Transaksi"),
                    rs.getString("Tanggal_Booking"),
                    rs.getString("Nama"),
                    rs.getString("Total"),
                    rs.getString("DP"),
                    rs.getString("Sisa_Pembayaran"),
                    rs.getString("Bayar"),
                    rs.getString("Final_Payment"),
                    rs.getString("Status")});

        }
        tbl_pembayaran.setModel(tableModel);
        JOptionPane.showMessageDialog(null,"Berhasil Menampilkan ke Table");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton6ActionPerformed

