    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Cart
        String panggil ="";
        try {
            Connection c = database.getkoneksi();
            Object st = txtnotrans.getText();
            panggil = st.toString();
            sql = "select * from tbl_booking_badminton where No_Transaksi = '" +panggil+"'";
            stat = c.createStatement();
            rs = stat.executeQuery(sql);
            while(rs.next()){
                txttanggal.setDate(rs.getDate("Tanggal_Booking"));
                txtnm.setText(rs.getString("Nama"));
                txttotal.setText(rs.getString("Total"));
                txtdp.setText(rs.getString("DP"));
                txtsisa.setText(rs.getString("Sisa_Pembayaran"));

            }
            JOptionPane.showMessageDialog(null, "Data Berhasil Dipanggil");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data tidak berhasil dipanggil");

        }
    }//GEN-LAST:event_jButton1ActionPerformed

